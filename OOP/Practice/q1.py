from Rational import Rational

a = Rational(-12, 5)
b = Rational(56, 64)

print('a = ' + str(a))
print('b = ' + str(b))

print(str(a) + '+' + str(b) + ' = ' + str(a+b))
print(str(a) + '+ 8' + ' = ' + str(a+8))
print(str(a) + '+ -8' + ' = ' + str(a+-8))

print(str(a) + '*' + str(b) + ' = ' + str(a*b))
print(str(a) + '* 8' + ' = ' + str(a*8))
print(str(a) + '* -8' + ' = ' + str(a*-8))
